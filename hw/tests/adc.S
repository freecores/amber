#include "amber_registers.h"

	.section .text
	.globl	main        
main:
        ldr     r0, Data0
        ldr     r1, Data1
        ldr     r2, Data2
        ldr     r3, Data3
        ldr     r4, Result0
        
        adcs    r8, r0, r1
        adc     r8, r8, r3
        
        @ check that overflow flag has been set
        mov     r5, pc
        and     r5, r5, #0xf0000000 @ clear non-flag bits
        cmp     r5,     #0x90000000 @ negative and overflow flags set
        movne   r10, #10
        bne     testfail
        
        @ check the result
        cmp     r8, r4
        movne   r10, #20
        bne     testfail
        
        
        b       testpass
        
        
testfail:
        ldr     r11, AdrTestStatus
        str     r10, [r11]
        b       testfail
        
testpass:             
        ldr     r11, AdrTestStatus
        mov     r10, #17
        str     r10, [r11]
        b       testpass
                

/* Write 17 to this address to generate a Test Passed message */
AdrTestStatus:  .word  ADR_AMBER_TEST_STATUS
Data0:          .word  0x40000000
Data1:          .word  0x40000000
Data2:          .word  0x10000000
Data3:          .word  0x10000000
Result0:        .word  0x90000000

